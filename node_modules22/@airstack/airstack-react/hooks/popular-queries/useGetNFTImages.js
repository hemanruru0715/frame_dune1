import { useQuery } from "../useQuery.js";
const query = (
  /* GraphQL */
  `
  query GetNFTImages(
    $address: Address!
    $tokenId: String!
    $blockchain: TokenBlockchain!
  ) {
    TokenNfts(
      input: {
        filter: { address: { _eq: $address }, tokenId: { _eq: $tokenId } }
        blockchain: $blockchain
      }
    ) {
      TokenNft {
        contentValue {
          image {
            original
            extraSmall
            large
            medium
            small
          }
        }
      }
    }
  }
`
);
function useGetNFTImages(variables) {
  return useQuery(query, variables);
}
export {
  useGetNFTImages
};
