import { useQuery } from "../useQuery.js";
const query = (
  /* GraphQL */
  `
  query TokenDetails($address: Address!, $blockchain: TokenBlockchain!) {
    Tokens(
      input: { filter: { address: { _eq: $address } }, blockchain: $blockchain }
    ) {
      Token {
        name
        symbol
        decimals
        totalSupply
        type
        baseURI
        address
        blockchain
        logo {
          large
          medium
          original
          small
        }
        projectDetails {
          collectionName
          description
          imageUrl
          discordUrl
          externalUrl
          twitterUrl
        }
      }
    }
  }
`
);
function useGetTokenDetails(variables) {
  return useQuery(query, variables);
}
export {
  useGetTokenDetails
};
